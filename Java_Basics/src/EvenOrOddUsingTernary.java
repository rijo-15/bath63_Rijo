
public class EvenOrOddUsingTernary {

	public static void main(String[] args) {
		
		
		char a = 'Z'; 
		
		switch(a) {
		case'A' : 
		case'E' : 
		case'I' : 
		case'O' : 
		case'U' :
			System.out.println("Vowel");
			break; 
		default: 
			System.out.println("Consonant");
			break; 
			
		}

		
	}

}
